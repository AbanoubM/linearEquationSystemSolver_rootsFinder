% --- Executes on button press in pushbutton2.
function pushbutton2_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton2 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
syms x;
set(handles.text12,'String',' ');
func=get(handles.edit1,'String');
low = str2double(get(handles.edit3,'String'));
high = str2double(get(handles.edit4,'String'));
accuracy = str2double(get(handles.edit5,'String'));
numIter = str2double(get(handles.edit2,'String'));
switch get(handles.popupmenu1,'Value');
      case 1.0 % General
               [first,second,flag] = guess(func);
               if(flag==0)
                [ xr , ea , final,msg] = false_pos( inline(func),first,second,accuracy,numIter);
                   set(handles.text12,'String',msg); 
               elseif (flag==1)
                   xr=first;
                   ea=0;
                   final=0;
               else 
                  [xl,xr, xu , ea , final,msg] = Bisection(low, high , accuracy , func ,numIter);
                   set(handles.text12,'String',msg);
               end
      case 2.0 % Bisection
        [xl,xr, xu , ea , final,msg] = Bisection(low, high , accuracy , func ,numIter);
         set(handles.text12,'String',msg);
        createfigure2(func,xl,xu);
      case 3.0 % False-position
         [ xr , ea , final,msg  ] = false_pos( inline(func),low,high,accuracy,numIter);
                   set(handles.text12,'String',msg);     
      case 4.0 % fixed point
          [ xr , ea , final,msg  ] = fixedpoint( func,low,accuracy,numIter);
                   set(handles.text12,'String',msg);
      case 5.0 % newton raphson
         [ xr , ea , final,msg] = newton_raph( inline(func),low,accuracy,numIter);
                   set(handles.text12,'String',msg);
      case 6.0 % secant
               [ xr , ea , final,msg]  = secant(func,low,high,accuracy,numIter);
                   set(handles.text12,'String',msg);
end
try
nums=1:length(xr);
out = [nums',xr',ea'];
set(handles.uitable1, 'Data',out, 'ColumnFormat',{'numeric'});
set(handles.text8, 'String',final);
catch
end
